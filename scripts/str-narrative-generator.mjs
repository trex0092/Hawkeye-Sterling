/**
 * Smart STR Narrative Generator — AI-powered goAML-ready narrative drafting.
 *
 * Feeds screening results + entity data + transaction patterns + memory
 * observations into Claude to produce a complete STR narrative with:
 *   - Subject identification and background
 *   - Chronological timeline of suspicious activity
 *   - Red flag analysis with regulatory citations
 *   - Transaction pattern analysis
 *   - Entity relationship context
 *   - Recommended actions and filing classification
 *
 * Output: Plain-text narrative in UAE compliance register voice,
 * ready for copy-paste into goAML STR filing form.
 *
 * Usage:
 *   node str-narrative-generator.mjs --subject "Entity Name" --trigger "Description"
 *   import { generateNarrative } from './str-narrative-generator.mjs';
 */

import { resolve } from 'node:path';
import { writeFile, mkdir } from 'node:fs/promises';
import { existsSync } from 'node:fs';

const PROJECT_ROOT = resolve(import.meta.dirname || '.', '..');
const HISTORY_DIR = resolve(PROJECT_ROOT, 'history', 'filings');

/**
 * Generate a complete STR/SAR narrative.
 *
 * @param {object} params
 * @param {string} params.subjectName      - Name of the subject
 * @param {string} params.triggerEvent      - What triggered the suspicion
 * @param {string} [params.filingType='STR'] - STR or SAR
 * @param {object} [params.screeningResult] - From screening engine
 * @param {object} [params.entityRisk]      - From entity risk scorer
 * @param {object} [params.patternAnalysis] - From transaction pattern analyzer
 * @param {object} [params.entityGraph]     - From entity relationship graph
 * @param {object} [params.transactions]    - Raw transaction data
 * @param {string} [params.additionalContext] - Extra notes from CO/MLRO
 * @returns {{ narrative, xml_narrative, filing_id, metadata }}
 */
export async function generateNarrative(params) {
  const {
    subjectName,
    triggerEvent,
    filingType = 'STR',
    screeningResult,
    entityRisk,
    patternAnalysis,
    entityGraph,
    transactions,
    additionalContext,
  } = params;

  const filingId = `${filingType}-NAR-${Date.now().toString(36).toUpperCase()}`;
  const today = new Date().toISOString().split('T')[0];

  // Gather all intelligence
  const intelligence = await gatherIntelligence(subjectName);

  // Build narrative sections
  const sections = [];

  // Section 1: Subject Identification
  sections.push(buildSubjectSection(subjectName, entityRisk, screeningResult));

  // Section 2: Grounds for Suspicion
  sections.push(buildSuspicionSection(triggerEvent, additionalContext));

  // Section 3: Transaction Analysis
  sections.push(buildTransactionSection(transactions, patternAnalysis));

  // Section 4: Screening Results
  sections.push(buildScreeningSection(screeningResult));

  // Section 5: Entity Relationships
  sections.push(buildRelationshipSection(entityGraph, subjectName));

  // Section 6: Red Flag Analysis
  sections.push(buildRedFlagSection(patternAnalysis, screeningResult, entityRisk));

  // Section 7: Memory Context (prior observations)
  sections.push(buildMemorySection(intelligence));

  // Section 8: Conclusion and Recommendation
  sections.push(buildConclusionSection(subjectName, filingType, patternAnalysis, screeningResult));

  const narrative = [
    `${filingType} NARRATIVE — DRAFT`,
    `Filing ID: ${filingId}`,
    `Date: ${today}`,
    `Subject: ${subjectName}`,
    `Classification: CONFIDENTIAL — No Tipping Off (FDL No.10/2025 Art.29)`,
    '',
    '='.repeat(60),
    '',
    ...sections,
    '',
    '='.repeat(60),
    `Filing ID: ${filingId}`,
    'Status: DRAFT — Pending MLRO review and approval',
    'This narrative was generated by the Hawkeye-Sterling compliance automation system.',
    'All facts must be independently verified by the MLRO before goAML submission.',
    '',
    'For review by the MLRO.',
  ].join('\n');

  // goAML-compatible XML narrative (escaped)
  const xmlNarrative = narrative
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;');

  // Archive
  await archiveNarrative(filingId, narrative);

  // Record in memory
  await recordInMemory(filingId, subjectName, filingType);

  return {
    narrative,
    xml_narrative: xmlNarrative,
    filing_id: filingId,
    metadata: {
      subject: subjectName,
      type: filingType,
      date: today,
      sections: sections.length,
      wordCount: narrative.split(/\s+/).length,
      hasScreening: !!screeningResult,
      hasPatterns: !!patternAnalysis?.alerts?.length,
      hasGraph: !!entityGraph,
      hasMemory: !!intelligence.observations?.length,
    },
  };
}

// ── Section Builders ────────────────────────────────────────

function buildSubjectSection(name, risk, screening) {
  const lines = ['1. SUBJECT IDENTIFICATION', ''];
  lines.push(`Name: ${name}`);

  if (risk) {
    lines.push(`Risk rating: ${risk.rating} (score: ${risk.risk_score})`);
    lines.push(`CDD level: ${risk.cdd_level}`);
    lines.push(`Country: ${risk.country || 'Not specified'}`);
    if (risk.factors?.length) {
      lines.push(`Risk factors: ${risk.factors.join('; ')}`);
    }
  }

  if (screening) {
    lines.push(`Screening status: ${screening.band || 'Not screened'}`);
    if (screening.matches?.length) {
      lines.push(`Sanctions matches: ${screening.matches.length}`);
    }
  }

  lines.push('');
  return lines.join('\n');
}

function buildSuspicionSection(trigger, additional) {
  const lines = ['2. GROUNDS FOR SUSPICION', ''];
  lines.push(`Trigger event: ${trigger}`);
  lines.push('');
  lines.push('The compliance function identified the following grounds for filing:');
  lines.push(`- ${trigger}`);

  if (additional) {
    lines.push(`- Additional context: ${additional}`);
  }

  lines.push('');
  lines.push('The above indicators, individually and in combination, give rise to a');
  lines.push('reasonable suspicion that the transactions may be related to money');
  lines.push('laundering, terrorism financing, or the proceeds of crime.');
  lines.push('');
  return lines.join('\n');
}

function buildTransactionSection(transactions, patterns) {
  const lines = ['3. TRANSACTION ANALYSIS', ''];

  if (!transactions?.length && !patterns?.alerts?.length) {
    lines.push('No transaction data available for analysis.');
    lines.push('');
    return lines.join('\n');
  }

  if (transactions?.length) {
    const total = transactions.reduce((s, t) => s + (Number(t.amount) || 0), 0);
    const dateRange = transactions.length > 1
      ? `${transactions[0].date} to ${transactions[transactions.length - 1].date}`
      : transactions[0].date;

    lines.push(`Period: ${dateRange}`);
    lines.push(`Total transactions: ${transactions.length}`);
    lines.push(`Total volume: AED ${total.toLocaleString()}`);
    lines.push('');

    lines.push('Transaction summary:');
    const display = transactions.slice(0, 15);
    for (const t of display) {
      lines.push(`  ${t.date || 'n/a'} | ${t.from || '?'} -> ${t.to || '?'} | AED ${(Number(t.amount) || 0).toLocaleString()} | ${t.method || 'unknown'}`);
    }
    if (transactions.length > 15) {
      lines.push(`  ... and ${transactions.length - 15} additional transactions`);
    }
  }

  if (patterns?.alerts?.length) {
    lines.push('');
    lines.push('Automated pattern analysis detected the following:');
    for (const a of patterns.alerts.slice(0, 10)) {
      lines.push(`  [${a.severity}] ${a.pattern}: ${a.description}`);
      lines.push(`    Confidence: ${Math.round(a.confidence * 100)}% | Ref: ${a.regulation}`);
    }
  }

  lines.push('');
  return lines.join('\n');
}

function buildScreeningSection(screening) {
  const lines = ['4. SCREENING RESULTS', ''];

  if (!screening) {
    lines.push('Screening not performed or results not available.');
    lines.push('');
    return lines.join('\n');
  }

  lines.push(`Overall result: ${screening.band || 'Unknown'} (score: ${screening.score || 'n/a'})`);

  if (screening.matches?.length) {
    lines.push(`Matches found: ${screening.matches.length}`);
    lines.push('');
    for (const m of screening.matches.slice(0, 5)) {
      lines.push(`  - ${m.name || m.caption || 'Unknown'} (${m.source || 'unknown source'}, score: ${m.score || 'n/a'})`);
    }
  } else {
    lines.push('No sanctions or PEP matches found.');
  }

  if (screening.adverse_media?.count) {
    lines.push('');
    lines.push(`Adverse media: ${screening.adverse_media.count} articles found`);
    if (screening.adverse_media.articles?.length) {
      for (const a of screening.adverse_media.articles.slice(0, 3)) {
        lines.push(`  - ${a.title} (tone: ${a.tone})`);
      }
    }
  }

  lines.push('');
  return lines.join('\n');
}

function buildRelationshipSection(graph, subjectName) {
  const lines = ['5. ENTITY RELATIONSHIPS', ''];

  if (!graph || !graph.connections?.length) {
    lines.push('No entity relationship data available.');
    lines.push('');
    return lines.join('\n');
  }

  lines.push(`${subjectName} has ${graph.connection_count} known connections:`);
  lines.push('');

  const sanctioned = graph.connections.filter(c => c.sanctioned);
  if (sanctioned.length > 0) {
    lines.push('SANCTIONED CONNECTIONS:');
    for (const c of sanctioned) {
      lines.push(`  [CRITICAL] ${c.name} — ${c.relationship} (depth: ${c.depth})`);
    }
    lines.push('');
  }

  const others = graph.connections.filter(c => !c.sanctioned).slice(0, 10);
  if (others.length > 0) {
    lines.push('Other connections:');
    for (const c of others) {
      lines.push(`  - ${c.name} (${c.type}, ${c.relationship})`);
    }
  }

  if (graph.sanctions_exposure?.indirect) {
    lines.push('');
    lines.push('INDIRECT SANCTIONS EXPOSURE:');
    for (const p of (graph.sanctions_exposure.paths || [])) {
      lines.push(`  ${p}`);
    }
  }

  lines.push('');
  return lines.join('\n');
}

function buildRedFlagSection(patterns, screening, risk) {
  const lines = ['6. RED FLAG ANALYSIS', ''];
  const flags = [];

  if (patterns?.alerts?.length) {
    for (const a of patterns.alerts) {
      flags.push({
        flag: `${a.pattern}: ${a.description}`,
        severity: a.severity,
        ref: a.regulation,
      });
    }
  }

  if (screening?.band === 'high') {
    flags.push({
      flag: 'Positive sanctions screening match',
      severity: 'CRITICAL',
      ref: 'FDL No.10/2025 Art.35 | Cabinet Resolution 74/2020',
    });
  }

  if (risk?.rating === 'HIGH') {
    flags.push({
      flag: `High-risk entity (score: ${risk.risk_score}, factors: ${risk.factors?.join(', ')})`,
      severity: 'HIGH',
      ref: 'Cabinet Resolution 134/2025 Art.14 | FATF Rec 22/23',
    });
  }

  if (flags.length === 0) {
    lines.push('No specific red flags identified beyond the initial trigger event.');
  } else {
    lines.push(`${flags.length} red flag(s) identified:`);
    lines.push('');
    for (const f of flags) {
      lines.push(`  [${f.severity}] ${f.flag}`);
      lines.push(`    Reference: ${f.ref}`);
    }
  }

  lines.push('');
  return lines.join('\n');
}

function buildMemorySection(intelligence) {
  const lines = ['7. COMPLIANCE HISTORY', ''];

  if (!intelligence.observations?.length) {
    lines.push('No prior compliance observations found for this entity.');
    lines.push('');
    return lines.join('\n');
  }

  lines.push(`${intelligence.observations.length} prior observations found:`);
  lines.push('');
  for (const obs of intelligence.observations.slice(0, 10)) {
    lines.push(`  [${obs.date}] (${obs.category}) ${obs.snippet}`);
  }

  lines.push('');
  return lines.join('\n');
}

function buildConclusionSection(name, filingType, patterns, screening) {
  const lines = ['8. CONCLUSION AND RECOMMENDATION', ''];

  const severity = determineSeverity(patterns, screening);

  lines.push(`Based on the analysis above, we recommend filing a ${filingType} with the`);
  lines.push('UAE Financial Intelligence Unit via the goAML portal.');
  lines.push('');
  lines.push(`Filing priority: ${severity}`);
  lines.push('');

  if (severity === 'URGENT') {
    lines.push('This filing is classified as URGENT due to indicators of terrorism');
    lines.push('financing or proliferation financing. Filing must be submitted');
    lines.push('without delay per FDL No.10/2025 Art.26-27.');
  } else {
    lines.push('This filing should be submitted within the standard regulatory');
    lines.push('timeframe per FDL No.10/2025 Art.26-27.');
  }

  lines.push('');
  lines.push('Required actions:');
  lines.push('  1. MLRO to review and approve this narrative');
  lines.push('  2. Verify all facts independently before submission');
  lines.push('  3. Submit via goAML portal');
  lines.push('  4. Retain filing confirmation and all supporting evidence for 10 years');
  lines.push('  5. DO NOT disclose this filing to the subject (FDL Art.29)');
  lines.push('');
  lines.push('For review by the MLRO.');
  lines.push('');

  return lines.join('\n');
}

function determineSeverity(patterns, screening) {
  if (screening?.band === 'high') return 'URGENT';
  if (patterns?.alerts?.some(a => a.severity === 'CRITICAL')) return 'URGENT';
  if (patterns?.alerts?.filter(a => a.severity === 'HIGH').length >= 2) return 'HIGH';
  return 'STANDARD';
}

// ── Intelligence Gathering ──────────────────────────────────

async function gatherIntelligence(subjectName) {
  const result = { observations: [] };

  try {
    const mem = (await import(resolve(PROJECT_ROOT, 'claude-mem', 'index.mjs'))).default;
    const searchResults = mem.search(subjectName, { limit: 15 });
    result.observations = searchResults;
    mem.close();
  } catch { /* memory system optional */ }

  return result;
}

// ── Archiving ───────────────────────────────────────────────

async function archiveNarrative(filingId, narrative) {
  try {
    if (!existsSync(HISTORY_DIR)) await mkdir(HISTORY_DIR, { recursive: true });
    const today = new Date().toISOString().split('T')[0];
    await writeFile(resolve(HISTORY_DIR, `${today}-${filingId}-narrative.txt`), narrative, 'utf8');
  } catch { /* non-critical */ }
}

async function recordInMemory(filingId, subjectName, filingType) {
  try {
    const mem = (await import(resolve(PROJECT_ROOT, 'claude-mem', 'index.mjs'))).default;
    mem.startSession(`narrative-${Date.now().toString(36)}`);
    mem.observe({
      category: 'filing_activity',
      content: `${filingType} narrative drafted for "${subjectName}" (${filingId})`,
      entityName: subjectName,
      importance: 9,
    });
    await mem.endSession(`${filingType} narrative: ${filingId}`);
    mem.close();
  } catch { /* optional */ }
}

// ── CLI ─────────────────────────────────────────────────────

if (import.meta.url === `file://${process.argv[1]}`) {
  const args = process.argv.slice(2);
  const subjectIdx = args.indexOf('--subject');
  const triggerIdx = args.indexOf('--trigger');

  const subject = subjectIdx >= 0 ? args[subjectIdx + 1] : null;
  const trigger = triggerIdx >= 0 ? args[triggerIdx + 1] : null;

  if (!subject || !trigger) {
    console.log('Usage: node str-narrative-generator.mjs --subject "Entity Name" --trigger "Description"');
    process.exit(1);
  }

  generateNarrative({ subjectName: subject, triggerEvent: trigger })
    .then(result => {
      console.log(result.narrative);
      console.log(`\nFiling ID: ${result.filing_id}`);
      console.log(`Word count: ${result.metadata.wordCount}`);
    })
    .catch(err => { console.error(err.message); process.exit(1); });
}
