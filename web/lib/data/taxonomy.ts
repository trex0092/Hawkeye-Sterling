function build(raw: readonly string[], category: TaxonomyCategory): TaxonomyEntry[] {
  const seen = new Set<string>();
  const out: TaxonomyEntry[] = [];
  for (const name of raw) {
    const id = `${category}-${slugifyTaxonomyName(name)}`;
    if (seen.has(id)) continue;
    seen.add(id);
    out.push({ id, name, category });
  }
  return out;
}

